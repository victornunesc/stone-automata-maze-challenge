function generateGrid(grid) {
  let newGrid = [];

  for (let row = 0; row < grid.length; row++) {
    let newRow = [];
    for (let col = 0; col < grid[row].length; col++) {
      let infectedNeighborsCells = countInfectedNeighborsCells(row, col, grid);

      if (grid[row][col] == 0) {
        if (infectedNeighborsCells > 1 && infectedNeighborsCells < 5) {
          newRow.push(1);
        } else {
          newRow.push(0);
        }
      } else if (grid[row][col] == 1) {
        if (infectedNeighborsCells > 3 && infectedNeighborsCells < 6) {
          newRow.push(1);
        } else {
          newRow.push(0);
        }
      } else {
        newRow.push(grid[row][col]);
      }
    }
    newGrid.push(newRow);
  }

  return newGrid;
}

function countInfectedNeighborsCells(row, col, grid) {
  let infectedNeighborsCells = 0;

  for (let i = row - 1; i <= row + 1; i++) {
    for (let j = col - 1; j <= col + 1; j++) {
      if (
        i >= 0 &&
        i < grid.length &&
        j >= 0 &&
        j < grid[row].length &&
        !(i == row && j == col)
      ) {
        if (grid[i][j] == 1) {
          infectedNeighborsCells++;
        }
      }
    }
  }

  return infectedNeighborsCells;
}

function findPath(startGrid) {
  let startNode = {
    map: startGrid,
    position: [0, 0],
    path: [],
    steps: 0,
    // visited: new Set(), not implemented
  };

  let movementsAllowed = [
    { row: -1, col: 0 },
    { row: 0, col: -1 },
    { row: 1, col: 0 },
    { row: 0, col: 1 },
  ];

  let queue = [startNode];

  while (queue.length > 0) {
    let currentNode = queue.pop();

    if (currentNode.position[0] == 64 && currentNode.position[1] == 84) {
      const path = currentNode.path.concat([currentNode.position]);
      const filename = "result.txt";
      const output = path.reduce((last, current, index) => {
        if (index === 0) return last;

        const [row, col] = path[index - 1];
        const [nextRow, nextCol] = current;

        if (nextRow > row) {
          last.push("D");
        } else if (nextRow < row) {
          last.push("U");
        } else if (nextCol > col) {
          last.push("R");
        } else if (nextCol < col) {
          last.push("L");
        }

        return last;
      }, []);

      fs.writeFile(filename, output.join(" "), (err) => {
        if (err) {
          console.error(err);
          return;
        }
        console.log(`Saída gravada em ${filename}`);
      });

      return currentNode.path.concat([currentNode.position]);
    }

    let newGrid = generateGrid(currentNode.map);

    for (let move of movementsAllowed) {
      if (
        currentNode.position[0] + move.row >= 0 &&
        currentNode.position[0] + move.row < newGrid.length &&
        currentNode.position[1] + move.col >= 0 &&
        currentNode.position[1] + move.col < newGrid[0].length &&
        (move.row != 0 || move.col != 0)
      ) {
        let nextPosition = [
          currentNode.position[0] + move.row,
          currentNode.position[1] + move.col,
        ];

        if (
          newGrid[nextPosition[0]][nextPosition[1]] == 0 ||
          newGrid[nextPosition[0]][nextPosition[1]] == 4
        ) {
          let newSteps = currentNode.steps + 1;
          let newPath = currentNode.path.concat([currentNode.position]);

          let newNode = {
            map: newGrid,
            position: nextPosition,
            path: newPath,
            steps: newSteps,
          };
          queue.push(newNode);
        }
      }
    }
  }

  return null;
}

